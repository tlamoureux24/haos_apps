#!/bin/bash
set -e

DATA_DIR="${DATA_DIR:-/data}"
JOBS_FILE="$DATA_DIR/jobs.json"
CRONTAB_DIR="${CRONTAB_DIR:-/var/spool/cron/crontabs}"
CRONTAB_FILE="$CRONTAB_DIR/root"

log_cron() {
    echo "[CRON] $*" > /proc/1/fd/1
}

mkdir -p "$CRONTAB_DIR"

{
    echo "# Generated by Rsync Manager"
    echo "SHELL=/bin/bash"
    echo "PATH=/usr/local/sbin:/usr/local/bin:/usr/sbin:/usr/bin:/sbin:/bin"
    echo ""
} > "$CRONTAB_FILE"

if [ ! -f "$JOBS_FILE" ] || ! jq -e 'type == "array"' "$JOBS_FILE" >/dev/null 2>&1; then
    log_cron "Aucun jobs.json valide, crontab vidée."
    chmod 600 "$CRONTAB_FILE"
    exit 0
fi

NORMALIZED_TMP=$(mktemp)
if /usr/local/bin/rsync_manager.sh normalize_jobs "$JOBS_FILE" "$NORMALIZED_TMP"; then
    cat "$NORMALIZED_TMP" > "$JOBS_FILE"
    chmod 666 "$JOBS_FILE"
else
    log_cron "Impossible de normaliser les ids des jobs, crontab vidée."
    rm -f "$NORMALIZED_TMP"
    chmod 600 "$CRONTAB_FILE"
    exit 0
fi
rm -f "$NORMALIZED_TMP"

COUNT=$(jq '. | length' "$JOBS_FILE")
ADDED=0

if [ "$COUNT" -gt 0 ]; then
    log_cron "Génération des règles depuis $JOBS_FILE ($COUNT jobs)."
fi

for i in $(seq 0 $((COUNT - 1))); do
    JOB_ID=$(jq -r ".[$i].id // \"\"" "$JOBS_FILE")
    CRON=$(jq -r ".[$i].cron // \"\"" "$JOBS_FILE")
    NAME=$(jq -r ".[$i].name // \"Job $i\"" "$JOBS_FILE")
    ENABLED=$(jq -r ".[$i].enabled // true" "$JOBS_FILE")

    if ! printf '%s' "$JOB_ID" | grep -Eq '^job_[A-Za-z0-9_-]+$'; then
        log_cron "Job $i ($NAME) ignoré: id invalide."
        continue
    fi

    if [ "$ENABLED" != "true" ]; then
        log_cron "Job $JOB_ID ($NAME) ignoré: désactivé."
        continue
    fi

    if [ -z "$CRON" ]; then
        log_cron "Job $JOB_ID ($NAME) ignoré: cron vide."
        continue
    fi

    FIELD_COUNT=$(printf '%s\n' "$CRON" | awk '{print NF}')
    if [ "$FIELD_COUNT" -ne 5 ]; then
        log_cron "Job $JOB_ID ($NAME) ignoré: expression cron invalide '$CRON'."
        continue
    fi

    {
        echo "# Job $JOB_ID: $NAME"
        echo "$CRON /usr/local/bin/rsync_manager.sh run $JOB_ID cron > /proc/1/fd/1 2>&1"
    } >> "$CRONTAB_FILE"
    ADDED=$((ADDED + 1))
done

echo "" >> "$CRONTAB_FILE"
chmod 600 "$CRONTAB_FILE"
log_cron "$ADDED règle(s) installée(s) dans $CRONTAB_FILE."

if pidof crond >/dev/null 2>&1; then
    kill $(pidof crond) 2>/dev/null || true
    log_cron "crond arrêté après régénération, s6 va le relancer."
else
    log_cron "crond n'est pas encore démarré."
fi
